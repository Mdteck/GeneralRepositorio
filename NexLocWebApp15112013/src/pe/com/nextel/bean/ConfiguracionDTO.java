package pe.com.nextel.bean;

public class ConfiguracionDTO {
	private String estadoAprov;
	private String urlAprov;
	private String upsidAprov;
	private String cslAprov;
	private String csnAprov;
	private String tceAprov;
	private String tccAprov;
	private String correoAprov;
	
	public String getEstadoAprov() {
		return estadoAprov;
	}
	public void setEstadoAprov(String estadoAprov) {
		this.estadoAprov = estadoAprov;
	}
	public String getUrlAprov() {
		return urlAprov;
	}
	public void setUrlAprov(String urlAprov) {
		this.urlAprov = urlAprov;
	}
	public String getUpsidAprov() {
		return upsidAprov;
	}
	public void setUpsidAprov(String upsidAprov) {
		this.upsidAprov = upsidAprov;
	}
	public String getCslAprov() {
		return cslAprov;
	}
	public void setCslAprov(String cslAprov) {
		this.cslAprov = cslAprov;
	}
	public String getCsnAprov() {
		return csnAprov;
	}
	public void setCsnAprov(String csnAprov) {
		this.csnAprov = csnAprov;
	}
	public String getTceAprov() {
		return tceAprov;
	}
	public void setTceAprov(String tceAprov) {
		this.tceAprov = tceAprov;
	}
	public String getTccAprov() {
		return tccAprov;
	}
	public void setTccAprov(String tccAprov) {
		this.tccAprov = tccAprov;
	}
	public String getCorreoAprov() {
		return correoAprov;
	}
	public void setCorreoAprov(String correoAprov) {
		this.correoAprov = correoAprov;
	}
}
